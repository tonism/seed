#!/usr/bin/env python3
"""Generate RSA-2048 public-key data blocks for the 286 secure tier.

Build 13 no longer bakes the provider leaf into SEED.SYS. Standard images ship the current
leaf as SEED/LEAF.DER; the 286 verifies that DER against the baked WR1 anchor, then adopts
the leaf key into runtime-only RSA workspace before the in-race ServerKeyExchange verify.

The production use of this script is therefore the durable WR1 anchor:

  python3 tools/gen-rsa-pinned-key.py --mode anchor --pem openai-chain.pem \
      --out targets/ibm_pc_5150/boot/core/rsa_anchor_wr1.inc
  python3 tools/gen-rsa-pinned-key.py --selftest      # prove the Montgomery math vs the bench

The default leaf mode is retained only for old rotation/debug notes. --mode anchor pins the
ISSUING CA (GTS WR1) key -- the durable anchor for silent auto-recertify. The off-race X.509
chain-verify (tools/x509/x509_verify.py is the offline oracle for it)
checks a freshly-rotated leaf against this anchor before adopting it, so a leaf rotation needs no
rebuild while trust still derives from a key we already hold. WR1 lives years; re-pin only on WR1
rotation. cert[1] of the -showcerts chain is the issuer.
"""
from __future__ import annotations
import argparse
import subprocess
import sys

WORDS = 128                 # 2048-bit operands, 16-bit little-endian limbs
NBITS = WORDS * 16          # 2048
R = 1 << NBITS              # Montgomery radix


def to_words(value: int) -> list[int]:
    return [(value >> (16 * i)) & 0xFFFF for i in range(WORDS)]


def mont_params(n: int) -> tuple[int, int]:
    if n.bit_length() != 2048:
        raise SystemExit(f"modulus is {n.bit_length()} bits, expected 2048 (RSA-2048 leaf)")
    if n % 2 == 0:
        raise SystemExit("modulus is even -- not an RSA modulus")
    n0inv = (-pow(n, -1, 1 << 16)) % (1 << 16)   # -N^-1 mod 2^16  (CIOS reduction multiplier)
    r2 = pow(R, 2, n)                            # R^2 mod N
    return n0inv, r2


def bignum_lines(name: str, value: int, per_line: int = 8) -> str:
    w = to_words(value)
    out = [f"{name}:"]
    for i in range(0, WORDS, per_line):
        out.append("    dw " + ",".join(str(x) for x in w[i:i + per_line]))
    return "\n".join(out) + "\n"


def nth_cert_pem(pem_path: str, index: int) -> str:
    """Return the Nth certificate (0 = leaf, 1 = issuer/WR1, ...) from a -showcerts chain as PEM."""
    blocks, cur, keep = [], [], False
    for line in open(pem_path).read().splitlines():
        if "BEGIN CERTIFICATE" in line:
            keep, cur = True, [line]
            continue
        if "END CERTIFICATE" in line:
            cur.append(line)
            blocks.append("\n".join(cur) + "\n")
            keep = False
            continue
        if keep:
            cur.append(line)
    if index >= len(blocks):
        raise SystemExit(f"chain has {len(blocks)} cert(s); no cert at index {index} "
                         f"(need the issuer cert in the chain — re-capture with -showcerts)")
    return blocks[index]


def _openssl_x509(pem_text: str, *flags: str) -> str:
    out = subprocess.run(["openssl", "x509", "-noout", *flags],
                         input=pem_text, capture_output=True, text=True, check=True).stdout.strip()
    return out


def modulus_from_pem_text(pem_text: str) -> int:
    out = _openssl_x509(pem_text, "-modulus")
    if not out.startswith("Modulus="):
        raise SystemExit(f"unexpected openssl output: {out[:40]!r}")
    return int(out[len("Modulus="):], 16)


def cert_meta(pem_text: str) -> str:
    return _openssl_x509(pem_text, "-subject", "-issuer", "-dates", "-fingerprint", "-sha256")


LEAF_HDR = [
    "; GENERATED by tools/gen-rsa-pinned-key.py -- legacy/debug leaf RSA-2048 public-key block.",
    "; Build 13 standard images ship the leaf as SEED/LEAF.DER instead of baking this block into",
    "; SEED.SYS. The 286 verifies that file against WR1 before adopting it for SKE verification.",
]
ANCHOR_HDR = [
    "; GENERATED by tools/gen-rsa-pinned-key.py --mode anchor -- the PINNED ISSUING-CA (GTS WR1)",
    "; RSA-2048 public key: the DURABLE anchor for silent auto-recertify. When the ~90-day leaf",
    "; rotates, the off-race X.509 chain-verify checks the freshly-presented leaf's signature",
    "; against THIS key (RSA-PKCS1-SHA256 over the leaf TBS) before adopting it -- so a leaf",
    "; rotation needs no rebuild, but trust still derives from an anchor we already hold.",
    "; WR1 itself lives years; RE-PIN only when WR1 rotates (a rare, human event). Regenerate:",
    ";   python3 tools/gen-rsa-pinned-key.py --mode anchor --pem <chain.pem> --out <this file>",
    ";",
    "; The on-device chain-verify copies wr1_n/r2/n0inv into the rsa_verify working constants",
    "; (rsa_one == 1 is shared) and reuses the leaf-verify scratch (rsa_sig/result/x/t) -- the",
    "; in-race leaf verify and the off-race chain-verify never run at the same time.",
]


def emit(n: int, host: str, meta: str, *, prefix: str = "rsa_", constants_only: bool = False) -> str:
    n0inv, r2 = mont_params(n)
    hdr = list(ANCHOR_HDR if prefix != "rsa_" else
               [line.replace("{host}", host) for line in LEAF_HDR])
    hdr += [";", f"; pinned {'issuer (anchor)' if constants_only else 'host'}: {host}"]
    hdr += ["; " + line for line in meta.splitlines()]
    hdr += [";"]
    if constants_only:
        hdr += [
            f"; e = 65537 (the on-device modexp hardcodes s^65537). ONLY {prefix}n is baked; the Montgomery",
            "; constants (r2, n0inv) are DERIVED at chain-verify time via rsa_adopt_derive -- this saves",
            "; ~514 B in the 286 module so the captured leaf fits the handshake-transient slot above it",
            "; (not the arena -> mid-chat recertify is safe). 128 x 16-bit little-endian limbs.",
        ]
    else:   # legacy/debug leaf mode
        hdr += [
            "; e = 65537 (the on-device modexp hardcodes s^65537). rsa_n/r2/one/n0inv are CONSTANTS",
            "; derived from the modulus; rsa_sig is the wire signature (set per handshake); the rest is",
            "; working scratch. Layout matches core/rsa_verify.inc (CIOS Montgomery, 128 x 16-bit limbs).",
        ]
    hdr += ["align 2"]
    if constants_only:   # anchor: bake only the modulus; rsa_adopt_derive computes r2 + n0inv on the fly
        body = [bignum_lines(prefix + "n", n)]
    else:
        body = [
            bignum_lines(prefix + "n", n),
            bignum_lines(prefix + "r2", r2),
            bignum_lines(prefix + "one", 1),
            f"{prefix}n0inv: dw {n0inv}\n",
            "rsa_sig: times 128 dw 0       ; the ServerKeyExchange signature (set from the wire)\n",
            "rsa_result: times 128 dw 0\n",
            "rsa_x: times 128 dw 0\n",
            "rsa_t: times 260 dw 0\n",
        ]
    return "\n".join(hdr) + "\n" + "".join(body) + "\n"


