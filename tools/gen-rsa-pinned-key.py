def _from_words(words: list[int]) -> int:
    return sum(w << (16 * i) for i, w in enumerate(words))


def selftest() -> int:
    n = _from_words(_BENCH_N_WORDS)
    n0inv, r2 = mont_params(n)
    ok_n0 = (n0inv == 31743)
    ok_r2 = (to_words(r2) == _BENCH_R2_WORDS)
    print(f"bench N0INV: got {n0inv}, expected 31743 -> {'OK' if ok_n0 else 'FAIL'}")
    print(f"bench R2 words match rsa_data_bench.inc -> {'OK' if ok_r2 else 'FAIL'}")
    return 0 if (ok_n0 and ok_r2) else 1


def main() -> int:
    ap = argparse.ArgumentParser()
    ap.add_argument("--mode", choices=["leaf", "anchor"], default="leaf",
                    help="leaf = pin the leaf key (in-race SKE verify, default); "
                         "anchor = pin the issuing CA (WR1) key for the off-race chain-verify")
    ap.add_argument("--pem", help="cert chain PEM (-showcerts); cert[0]=leaf, cert[1]=issuer (WR1)")
    ap.add_argument("--modulus", help="hex RSA modulus (alternative to --pem)")
    ap.add_argument("--cert-index", type=int,
                    help="which chain cert to pin (default 0=leaf for --mode leaf, 1=issuer for anchor)")
    ap.add_argument("--out", help="output .inc path (default: stdout)")
    ap.add_argument("--host", default="api.openai.com")
    ap.add_argument("--prefix", help="symbol prefix (default rsa_ for leaf, wr1_ for anchor)")
    ap.add_argument("--selftest", action="store_true", help="prove the Montgomery math vs the bench")
    args = ap.parse_args()

    if args.selftest:
        return selftest()

    anchor = (args.mode == "anchor")
    prefix = args.prefix or ("wr1_" if anchor else "rsa_")
    index = args.cert_index if args.cert_index is not None else (1 if anchor else 0)

    if args.modulus:
        n, meta = int(args.modulus, 16), f"modulus: (provided directly, host {args.host})"
    elif args.pem:
        pem_text = nth_cert_pem(args.pem, index)
        n, meta = modulus_from_pem_text(pem_text), cert_meta(pem_text)
    else:
        raise SystemExit("need --pem or --modulus (or --selftest)")

    text = emit(n, args.host, meta, prefix=prefix, constants_only=anchor)
    if args.out:
        with open(args.out, "w") as f:
            f.write(text)
        print(f"wrote {args.out} ({args.mode}: {n.bit_length()}-bit modulus, "
              f"prefix {prefix!r}, chain index {index})")
    else:
        sys.stdout.write(text)
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
